// ============================================================================
// ИНИЦИАЛИЗАЦИЯ PWM (ШИМ) ДЛЯ УПРАВЛЕНИЯ ТРАНЗИСТОРАМИ
// Используем периферию LEDC (LED Control) в ESP32-C3
// ============================================================================
void Controller::initPWM()
{
    ESP_LOGI(TAG, "Инициализация PWM...");
    
    // ------------------------------------------------------------------------
    // Что такое LEDC?
    // LEDC (LED Control) - аппаратный модуль ESP32 для генерации PWM сигнала.
    // Преимущества перед программным PWM:
    // - Не нагружает процессор
    // - Точная частота
    // - Работает независимо от основного кода
    // ------------------------------------------------------------------------
    
    // ------------------------------------------------------------------------
    // Конфигурация таймера PWM
    // timer_conf - структура с настройками таймера
    // ------------------------------------------------------------------------
    ledc_timer_config_t timer_conf = {};
    timer_conf.duty_resolution = LEDC_TIMER_10_BIT;  // Разрешение 10 бит (0-1023)
    timer_conf.freq_hz = 5000;                       // Частота PWM 5 кГц
    timer_conf.speed_mode = LEDC_LOW_SPEED_MODE;     // Режим низкой скорости
    timer_conf.timer_num = LEDC_TIMER_0;             // Номер таймера (0-3)
    timer_conf.clk_cfg = LEDC_AUTO_CLK;              // Автоматический выбор тактирования
    
    // Применяем конфигурацию таймера
    ESP_ERROR_CHECK(ledc_timer_config(&timer_conf));
    ESP_LOGI(TAG, "  Таймер настроен: 10 бит, %d Гц", timer_conf.freq_hz);
    
    // ------------------------------------------------------------------------
    // Массив пинов для 4 каналов PWM
    // ------------------------------------------------------------------------
    const gpio_num_t pwm_pins[] = {PWM_PIN_0, PWM_PIN_1, PWM_PIN_2, PWM_PIN_3};
    constexpr int NUM_PWM_CHANNELS = 4;
    
    // ------------------------------------------------------------------------
    // Конфигурация каждого канала
    // channel_conf - структура с настройками канала
    // ------------------------------------------------------------------------
    ledc_channel_config_t channel_conf = {};
    channel_conf.gpio_num = GPIO_NUM_NC;  // Будем менять для каждого канала
    channel_conf.speed_mode = LEDC_LOW_SPEED_MODE;
    channel_conf.channel = LEDC_CHANNEL_0;  // Будем менять для каждого канала
    channel_conf.intr_type = LEDC_INTR_DISABLE;  // Прерывания отключены
    channel_conf.timer_sel = LEDC_TIMER_0;       // Используем таймер 0
    channel_conf.duty = 0;                       // Начальная скважность 0% (выключено)
    channel_conf.hpoint = 0;                     // Точка изменения (для фазовой коррекции)
    
    // Настраиваем все 4 канала
    for (int i = 0; i < NUM_PWM_CHANNELS; ++i)
    {
        channel_conf.gpio_num = pwm_pins[i];
        channel_conf.channel = static_cast<ledc_channel_t>(i);
        
        ESP_ERROR_CHECK(ledc_channel_config(&channel_conf));
        ESP_LOGI(TAG, "  Канал %d: GPIO%d", i, pwm_pins[i]);
    }
    
    ESP_LOGI(TAG, "PWM инициализирован успешно");
}

